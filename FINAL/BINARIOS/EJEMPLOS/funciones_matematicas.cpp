#include <iostream>
#include <cmath>
using namespace std;

int main() {
    double x = 16.0;
    double base = 2.0;
    double exponente = 3.0;

    cout << sqrt(x) << endl;
    cout << pow(base, exponente) << endl;
    cout << fabs(-3.5) << endl;
    cout << round(3.6) << endl;
    cout << floor(3.9) << endl;
    cout << ceil(3.1) << endl;

    return 0;
}
